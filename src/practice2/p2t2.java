public class p2t2 {
    public static void main(String[] args) {
        for(int i =1;i<=20;i+=2) System.out.println(i);
        int i =1;
        while(i<20){
            System.out.println(i);
            i+=2;
        }
        int j =1;
        do{
            System.out.println(j);
            j+=2;
        }while(j<20);
    }
}
